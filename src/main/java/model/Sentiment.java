package model;

public class Sentiment {

    private Float polarity;
    private String type;

    public Float getPolarity() {
        return polarity;
    }

    public void setPolarity(Float polarity) {
        this.polarity = polarity;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
